
package com.zlf.appmaster.model.extra;

public class AppWallUrlBean {
	private String url;
	private String id;
	public AppWallUrlBean(String url, String id) {
		super();
		this.url = url;
		this.id = id;
	}
	public AppWallUrlBean() {
		super();
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
   

}
